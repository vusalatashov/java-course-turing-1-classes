package az.edu.turing.module01.lesson05;

public class ForLoopApp {

    public static void main(String[] args) {
        //input
        int i = 0;
        //proses
        for (; i <= 10; i++) {
            System.out.println(i);
        }
        //output
        System.out.println(i);
        System.out.println(i++);
        System.out.println(i);
    }
}
